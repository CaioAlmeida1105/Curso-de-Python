#2. Faça um Programa que peça um número e então mostre a mensagem O número informado foi [número].

num=int(input('Digite um numero:'))     #Aqui foi criada uma VAR (Váriavel) chamada "num", que receberá informações to tipo primitivo inteiro (INT) do usuário através da função "input".

print(f'O numero informado foi [{num}]')      # A função F (f-tring) foi usada para colocar a váriavel dentro do texto.
