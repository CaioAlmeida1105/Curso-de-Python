#5. Faça um Programa que converta metros para centímetros.

m=float(input('Digite o valor em metros: '))        #Criando VAR do tipo FLOAT para receber informações do usuário.

cm=float(m*100)         #Cirando uma VAR do tipo FLOAT que contém o resultando da conversão de M (metros) para CM (centímetros)  

print(f'O valor em cm é: {cm:.2f}')     #Mostrando resultado para o usuário, a formatação ":.2f" foi usadara para omitir os numeros que aparecem depois de duas casas após o "." na mensagem será exibida.