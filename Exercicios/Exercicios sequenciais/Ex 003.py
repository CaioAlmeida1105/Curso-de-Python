#3. Faça um Programa que peça dois números e imprima a soma.

num1=int(input("Digite o primeiro número:"))       
num2=int(input('Digite o segundo número:'))         #Criando VAR do tipo INT (Números inteiros) para receber informações do usuário.

soma=num1+num2      #Criando uma VAR que contém o resultado da soma das VAR anteriores

print(f'A soma é: {soma}')      #Mostrando o resultado para o usuário.